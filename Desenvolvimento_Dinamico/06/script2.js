var diaSem = 4;

switch(diaSem){ //compara valor da variavel com as condições
    case 1:         //Caso diaSem = 1 faz a ação
        console.log("Domingo");
        break;
    case 2:
        console.log("Segunda");
        break;
    case 3:
        console.log("Terça");
        break;
    default:
        console.log("Esse dia da semana não existe!");
}